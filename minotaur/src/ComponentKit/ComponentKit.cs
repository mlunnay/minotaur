/// **ComponentKit** provides a set of **C#** interfaces and classes that are useful when you need to maintain an
/// **in-memory database** of **entity** composites.
/// 
/// Features include: **triggers**, for handling the processing of components, **dependency injection**, and some
/// very basic **inter-component messaging**.
/// 
/// ###How does it work?
/// 
/// The *entity/component* model is popularly used in game programming, and the scripting in [Unity3D](http://www.unity3d.com) 
/// is a great example of how it works in action.
